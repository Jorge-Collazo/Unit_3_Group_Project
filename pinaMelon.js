"use strict";

function changeBg(color){
    document.body.style.background = color;
   
    if (color == "strawberry"){
        document.getElementsByTagName("html")[0].style.background = "red";
    }
    else if (color == "tea"){
        document.getElementsByTagName("html")[0].style.background = "red";

    }
    else if (color == "blueRaspberry"){
        document.getElementsByTagName("html")[0].style.background = "red";
    }
    else if (color == "lemon"){
        document.getElementsByTagName("html")[0].style.background = "red";
    }
}